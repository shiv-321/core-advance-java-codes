package com.javapracticeshiv;

public class Ch_5_While_loop {
    public static void main(String[] args) {
        int i = 100;
        while (i <= 200){
            System.out.println(i);
            i++;

        }
        int j = 100;
        do{
            System.out.println(j);
            j++;
        }while(j<=200);


    }
}
